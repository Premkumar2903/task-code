
const fruits = ['Apple', 'orange' , 'Mango']

console.log(fruits)

//push adds item to end
fruits.push('Gva')
console.log(fruits)


//pop removes item from end
fruits.pop()
console.log(fruits)


//unshift adds item in front
fruits.unshift('Strawberry')
console.log(fruits)


//shift removes item in front
fruits.shift()
console.log(fruits)